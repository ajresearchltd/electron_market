CREATE POLICY "Guests can select own ai chat sessions"
  ON public.ai_chat_sessions
  FOR SELECT
  TO anon
  USING (
    guest_session_id IS NOT NULL
    AND EXISTS (
      SELECT 1 FROM public.ai_api_config
      WHERE ai_api_config.is_enabled = true
        AND ai_api_config.allow_guest_chat = true
    )
  );

