CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- Electron Market - public countries table for supplier registration
-- Manual step: run this file in Supabase SQL Editor.

CREATE TABLE IF NOT EXISTS public.countries (
  country_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  iso2 text NOT NULL UNIQUE,
  iso3 text UNIQUE,
  name text NOT NULL UNIQUE,
  is_active boolean NOT NULL DEFAULT true,
  sort_order integer,
  created_at timestamptz NOT NULL DEFAULT now()
);

ALTER TABLE public.countries ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Anyone can select active countries" ON public.countries;
CREATE POLICY "Anyone can select active countries"
  ON public.countries
  FOR SELECT
  TO public
  USING (is_active = true);

